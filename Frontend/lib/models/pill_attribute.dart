class PillAttribute {
  String pillId;
  String name;
  String howToUse;
  String effect;
  String warning;
  String howToStore;
  String sideEffect;
  String interaction;
  String imgPath;

  PillAttribute(
      {
        required this.pillId,
        required this.name,
        required this.howToUse,
        required this.effect,
        required this.warning,
        required this.howToStore,
        required this.sideEffect,
        required this.interaction,
        required this.imgPath
      });
}
